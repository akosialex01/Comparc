    public String generateBinary(String register)
    {
        String str[] = register.split("R");
        regTemp = "";
        regTemp = regTemp.concat(str[1]);
        regNum  = Integer.parseInt(regTemp);
        
        String temp = Integer.toBinaryString(regNum);
        String temp2 = "";
        
        
        for(int i = 0; i < 5 - temp.length(); i++)
            temp2 = temp2.concat("0");
        
        temp2 = temp2.concat(temp);
        
        return temp2;
        
    }
    
